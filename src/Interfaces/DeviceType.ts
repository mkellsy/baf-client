/**
 * Lists all supported device types.
 */
export enum DeviceType {
    /**
     * Dimmable downlight device.
     */
    Downlight = "Downlight",

    /**
     * Fan device.
     */
    Fan = "Fan",

    /**
     * Humidity sensor.
     */
    Humidity = "Humidity",

    /**
     * Occupancy sensor.
     */
    Occupancy = "Occupancy",

    /**
     * Temperature sensor.
     */
    Temperature = "Temperature",

    /**
     * Dimmable uplight device.
     */
    Uplight = "Uplight",

    /**
     * UV light device.
     */
    UVC = "UVC",
}
